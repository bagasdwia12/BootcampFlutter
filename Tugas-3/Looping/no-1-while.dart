void main(){
  print("Looping Pertama");
  int index=2;
  while (index <= 20) {
    print(index.toString() + " I Love Coding");
    index += 2;
  }
  
  print("\nLooping Kedua");
  int indeks=20;
  while (indeks >= 2) {
    print(indeks.toString() + " I Will Become Mobile Developer");
    indeks -= 2;
  }
}